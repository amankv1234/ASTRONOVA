def verify_physical_consistency():
    print("--- Verifying Physical Consistency ---")
    # Higher X-ray flux should yield higher flare probability.
    # We can mock this check or test it against the engine.
    from services.forecasting.services.inference_engine import InferenceEngine
    engine = InferenceEngine()
    
    # Low flux
    input_low = np.ones((1, 10, 15), dtype=np.float32)
    input_low[0, :, 0] = -8  # log soft flux
    out_low = engine.predict(input_low)
    prob_low = out_low["prediction"]["flare_probability"]
    
    # High flux
    input_high = np.ones((1, 10, 15), dtype=np.float32)
    input_high[0, :, 0] = -4  # log soft flux
    out_high = engine.predict(input_high)
    prob_high = out_high["prediction"]["flare_probability"]
    
    consistent = prob_high > prob_low
    print(f"Low Flux Prob: {prob_low:.4f} | High Flux Prob: {prob_high:.4f}")
    print(f"Physical Consistency: {'PASS' if consistent else 'FAIL'}")
    return consistent, prob_low, prob_high

