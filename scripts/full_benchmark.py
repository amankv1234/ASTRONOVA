import os
import time
import psutil
import json
import numpy as np
import sys
sys.path.append(os.path.abspath("."))

def benchmark_inference():
    print("--- Benchmarking Inference ---")
    from services.forecasting.services.inference_engine import InferenceEngine
    engine = InferenceEngine()
    
    # Warmup
    dummy = np.random.randn(1, 10, 15).astype(np.float32)
    for _ in range(5):
        engine.predict(dummy)
        
    latencies = []
    throughput_samples = 100
    
    start_total = time.time()
    for _ in range(throughput_samples):
        start = time.time()
        engine.predict(dummy)
        latencies.append((time.time() - start) * 1000)
        
    total_time = time.time() - start_total
    throughput = throughput_samples / total_time
    
    metrics = {
        "mean_latency": np.mean(latencies),
        "p90": np.percentile(latencies, 90),
        "p95": np.percentile(latencies, 95),
        "p99": np.percentile(latencies, 99),
        "throughput": throughput
    }
    print(f"Mean Latency: {metrics['mean_latency']:.2f} ms")
    print(f"Throughput: {metrics['throughput']:.2f} req/s")
    return metrics

def get_model_sizes():
    print("\n--- Measuring Model Sizes ---")
    files = {
        "BiLSTM": "models/lstm/best.pt",
        "XGBoost": "models/xgboost/model.pkl",
        "LightGBM": "models/lightgbm/model.pkl"
    }
    
    sizes = {}
    for name, path in files.items():
        if os.path.exists(path):
            size_mb = os.path.getsize(path) / (1024 * 1024)
            sizes[name] = size_mb
            print(f"{name}: {size_mb:.2f} MB")
        else:
            sizes[name] = 0.0
            print(f"{name}: NOT FOUND")
            
    sizes["Ensemble Total"] = sum(sizes.values())
    return sizes
    
def get_system_util():
    process = psutil.Process(os.getpid())
    return {
        "cpu": process.cpu_percent(interval=1.0),
        "memory_mb": process.memory_info().rss / (1024 * 1024)
    }

def main():
    os.makedirs("reports", exist_ok=True)
    
    sys_metrics = get_system_util()
    inf_metrics = benchmark_inference()
    sizes = get_model_sizes()
    
    report = f"""# System Benchmark Report

## 1. Inference Performance
- **Throughput**: {inf_metrics['throughput']:.2f} requests/sec
- **Mean Latency**: {inf_metrics['mean_latency']:.2f} ms
- **p90 Latency**: {inf_metrics['p90']:.2f} ms
- **p95 Latency**: {inf_metrics['p95']:.2f} ms
- **p99 Latency**: {inf_metrics['p99']:.2f} ms

## 2. Resource Utilization
- **CPU Utilization**: {sys_metrics['cpu']:.1f}%
- **Memory Footprint**: {sys_metrics['memory_mb']:.2f} MB

## 3. Model Sizes
- **BiLSTM**: {sizes['BiLSTM']:.2f} MB
- **XGBoost**: {sizes['XGBoost']:.2f} MB
- **LightGBM**: {sizes['LightGBM']:.2f} MB
- **Total Ensemble Engine**: {sizes['Ensemble Total']:.2f} MB

*Generated by full_benchmark.py*
"""
    with open("reports/system_benchmark.md", "w", encoding="utf-8") as f:
        f.write(report)
    print("\nReport written to reports/system_benchmark.md")

if __name__ == "__main__":
    main()
